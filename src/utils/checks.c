#include "../../inc/utils.h"

bool	string_all_spaces(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] != ' ' && str[i] != '\t' && str[i] != '\n' && str[i] != '\r')
			return (false);
		i++;
	}
	return (true);
}
